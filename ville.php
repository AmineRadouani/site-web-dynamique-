     <p>L’une des quatre villes impériales du Maroc, Marrakech est un centre commercial clé depuis de nombreuses années. La position de la ville fait partie intégrante du commerce des marchandises dans le pays, et son accent sur le commerce a façonné la ville. Jetez un coup d’œil à l’emblématique mosquée Koutoubia, qui date du 12e siècle et est un symbole de la vieille ville. L’exquis palais Bahia du 19e siècle se distingue également par son design somptueux.

Marrakech est une ville animée et trépidante. La place du marché principal, Jemaa el-Fnaa, dans la médina de la ville, est une expérience chaude et exiguë pour la plupart des gens. Tout et n’importe quoi est en vente, des singes aux instruments de musique.

Les marchands interpellent les clients pour les chahuter et les séduire – le troc de marchandises est la norme. À la nuit tombée, la place principale se transforme et laisse place à des stands de nourriture fumante. Bien que cela ne semble pas possible, la ville devient encore plus une expérience de voyage animée, dynamique et exubérante.</p>
