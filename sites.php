<div id="Kasbah"> 
     <h1>Kasbah du Pacha el Glaoui</h1> <hr>
     <!-- <div class="container"> -->
    <!-- <div class="image-and-text"> -->
        <img src="kasbah1.jpg" alt="Image"> <img src="kasbah4.jpg" alt="Image"> <img src="kasbah3.jpg" alt="Image"> 
        <div class="container">
    <div class="image-and-text">
        <img src="kasbah2.jpg" alt="Image">
        
     <p>Le passage des caravanes marchandes, qui reliaient le désert aux grandes villes situées de l'autre côté de l'Atlas, et la proximité des mines de sel ont fait la richesse des pachas qui habitaient Télouet.

La kasbah actuelle a été construite à partir de 1860 par les Glaoua1, à côté d'une ancienne kasbah dont les restes sont encore visibles[réf. nécessaire]. Elle fut par la suite considérablement agrandie au cours de la première moitié du xxe siècle par Thami El Glaoui. Il y fit également venir des ateliers de tapissiers, dans une région où cet artisanat n'était pas encore développé2.

Le soutien apporté aux Français par Thami El Glaoui, souvent appelé le Glaoui durant l'occupation française au Maroc, lui vaut le soutien réciproque des autorités coloniales françaises. En 1927, le militaire français Georges Spillmann, futur général, est installé à proximité du château, ayant été nommé chef du poste de Telouet3.

À l'apogée de sa puissance, le Glaoui a un pouvoir considérable, faisant de lui l'une des principales personnalités du pays. La kasbah de Télouet est alors qualifiée en 1938 par l'historien Henri Terrasse de « grand ksar seigneurial »4.

Mais son engagement pro-Français se retourne contre lui lors de la montée de plus en plus croissante vers l'indépendance du Maroc. Il sombre dans l'oubli et meurt à Marrakech en 1956. Le cortège funèbre est alors présidé par le prince héritier Moulay Hassan ben Mohammed, futur souverain du Maroc en tant que Hassan II.

Après 1956, un lieu abandonné
A son décès, c'est Si Brahim, son fils aîné, qui est le caïd de Télouet5. Mais en 1955, Si Brahim avait quitté définitivement le Maroc pour s'installer en France auprès de son épouse Cécile Aubry. En juillet 1956, la kasbah est occupée par trois cents hommes de l'Armée de libération nationale6. Elle est ensuite pillée7.

Depuis l'abandon de Kasbah par les Glaoui, l'Etat marocain a laissé la forteresse en l'état. Au début du XXIe siècle, elle tombait en ruines8. En 2015, un journaliste qui le visitait notait le contraste frappant entre l'aspect décrépit de l'extérieur et les intérieurs ornés avec détail9.

La chanteuse marocaine Oum, quand elle était étudiante à l'Ecole nationale d’architecture à Rabat, travaille sur la restauration de la kasbah de Télouet. Elle se souvient d'« un bâtiment curieux, très original, car construit sur plusieurs époques. Je faisais des relevés – car il n’y avait pas de plans – tout en commençant à gagner un peu d’argent en chantant (...). Il n’y avait pas de documents graphiques de la kasbah. Je les ai faits. J’ai rempli ma mission »10.. </p>
