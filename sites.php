     <p>Le cap Spartel ou Ras Spartel (en arabe : رأس سبارتيل) est un promontoire de la côte du Maroc, situé à l'entrée sud du détroit de Gibraltar, à 14 kilomètres à l'ouest de Tanger. Face au cap Spartel, à 44 km au nord, le cap Trafalgar marque l'entrée nord du détroit, sur la côte espagnole.

Le cap Spartel est souvent indiqué par erreur comme le point le plus au nord de l'Afrique. En réalité, c'est le cap Angela (Tunisie) et le cap Spartel est seulement le point le plus au nord de la côte atlantique africaine et également le point le plus à l'ouest de la côte méditerranéenne méridionale.</p>
