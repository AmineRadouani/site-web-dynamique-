     <p>Vous pouvez vous rendre au port pour déguster les pêches du jour, où les marchands ont aménagés des grills pour pouvoir servir des poissons et autres crustacés tout juste sortis de l’océan. Venez avec un esprit aventurier et un grand appétit, car vous seriez bien servis par les experts locaux., Jemaa el-Fnaa, dans la médina de la ville, est une expérience chaude et exiguë pour la plupart des gens. Tout et n’importe quoi est en vente, des singes aux instruments de musique.

Les marchands interpellent les clients pour les chahuter et les séduire – le troc de marchandises est la norme. À la nuit tombée, la place principale se transforme et laisse place à des stands de nourriture fumante. Bien que cela ne semble pas possible, la ville devient encore plus une expérience de voyage animée, dynamique et exubérante.</p>
